/** @param {string} mode */
export function normalizeDisplayMode(mode) {
  const s = String(mode ?? "").trim();
  if (s === DISPLAY_MODES.bar) return DISPLAY_MODES.bar;
  if (s === DISPLAY_MODES.area) return DISPLAY_MODES.area;
  if (s === DISPLAY_MODES.gradientBar) return DISPLAY_MODES.gradientBar;
  if (s === DISPLAY_MODES.glowLine) return DISPLAY_MODES.glowLine;
  if (s === DISPLAY_MODES.glowCircle) return DISPLAY_MODES.glowCircle;
  if (s === DISPLAY_MODES.radial) return DISPLAY_MODES.radial;
  if (s === DISPLAY_MODES.waterfall) return DISPLAY_MODES.waterfall;
  if (s === DISPLAY_MODES.dotRing) return DISPLAY_MODES.dotRing;
  if (s === DISPLAY_MODES.oscilloscope) return DISPLAY_MODES.oscilloscope;
  if (s === DISPLAY_MODES.obliqueBar) return DISPLAY_MODES.obliqueBar;
  if (s === DISPLAY_MODES.depthLayers) return DISPLAY_MODES.depthLayers;
  if (s === DISPLAY_MODES.isometricSkyline) return DISPLAY_MODES.isometricSkyline;
  if (s === DISPLAY_MODES.ring3d) return DISPLAY_MODES.ring3d;
  return DISPLAY_MODES.line;
}

/** @param {string} value */
export function normalizeDepthLayersRenderStyle(value, fallback = "line") {
  const s = String(value ?? "").trim();
  if (s === "bar") return "bar";
  if (s === "line") return "line";
  return fallback;
}

export function clampInt(value, min, max) {
  const n = Math.round(Number(value));
  if (!Number.isFinite(n)) return min;
  return Math.min(max, Math.max(min, n));
}

export function parseBoolean(value, fallback = false) {
  if (typeof value === "boolean") return value;
  if (value === "true") return true;
  if (value === "false") return false;
  return fallback;
}

export function normalizeBarOrientation(value, fallback = BAR_ORIENTATIONS.horizontal) {
  const s = String(value ?? "").trim();
  if (s === BAR_ORIENTATIONS.vertical) return BAR_ORIENTATIONS.vertical;
  if (s === BAR_ORIENTATIONS.horizontal) return BAR_ORIENTATIONS.horizontal;
  return fallback;
}

export function normalizeBarPeakHoldMode(value, fallback = PEAK_HOLD_MODES.single) {
  const s = String(value ?? "").trim();
  if (s === PEAK_HOLD_MODES.off) return PEAK_HOLD_MODES.off;
  if (s === PEAK_HOLD_MODES.both) return PEAK_HOLD_MODES.both;
  if (s === PEAK_HOLD_MODES.single) return PEAK_HOLD_MODES.single;
  return fallback;
}

/**
 * 读取峰值保持线模式；兼容旧版 boolean 存储 `barPeakHold`。
 * @param {Storage} ls
 * @param {string} windowLabel
 */
export function readBarPeakHoldMode(ls, windowLabel) {
  const modeRaw = readWindowStorageString(ls, windowLabel, "barPeakHoldMode");
  if (modeRaw === PEAK_HOLD_MODES.off || modeRaw === PEAK_HOLD_MODES.single || modeRaw === PEAK_HOLD_MODES.both) {
    return modeRaw;
  }
  const legacyRaw = readWindowStorageString(ls, windowLabel, "barPeakHold");
  if (legacyRaw != null && legacyRaw !== "") {
    return parseBoolean(legacyRaw, true) ? PEAK_HOLD_MODES.single : PEAK_HOLD_MODES.off;
  }
  return DEFAULT_CONFIG.bar.peakHoldMode;
}

