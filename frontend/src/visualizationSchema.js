/** 仅允许主窗与频谱副窗作为「每窗配置」的存储分区。 */
export function normalizeSpectrumWindowLabel(label) {
  const s = String(label ?? "").trim();
  if (s === "main" || s.startsWith("spectrum-")) {
    return s;
  }
  return "main";
}

